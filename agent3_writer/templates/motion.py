"""
Шаблони клопотань
"""

MOTION_TYPES = {
    "security": "Клопотання про забезпечення позову",
    "restore_deadline": "Клопотання про поновлення процесуального строку",
    "evidence": "Клопотання про витребування доказів",
    "expert": "Клопотання про призначення експертизи",
    "adjournment": "Клопотання про відкладення розгляду справи",
}

SYSTEM_PROMPTS = {
    "security": """Склади клопотання про забезпечення позову відповідно до ЦПК/КАС/ГПК України.
Вкажи: предмет забезпечення, підстави (реальна загроза невиконання рішення),
пропонований захід забезпечення. Мова: українська.""",

    "restore_deadline": """Склади клопотання про поновлення процесуального строку.
Вкажи: який строк пропущено, поважні причини пропуску, докази поважності.
Посилайся на відповідні норми ЦПК/КАС/ГПК. Мова: українська.""",

    "evidence": """Склади клопотання про витребування доказів.
Вкажи: які докази, від кого, чому вони важливі для справи,
чому сторона не може отримати їх самостійно. Мова: українська.""",

    "expert": """Склади клопотання про призначення судової експертизи.
Вкажи: вид експертизи, питання для експерта, установу або особу-експерта (якщо відомо).
Обґрунтуй необхідність. Мова: українська.""",

    "adjournment": """Склади клопотання про відкладення розгляду справи.
Вкажи: причини відкладення, на який строк. Мова: українська.""",
}


def get_motion_prompt(motion_type: str, case_data: str) -> tuple[str, str]:
    """Повертає (system_prompt, user_message) для конкретного типу клопотання"""
    system = SYSTEM_PROMPTS.get(
        motion_type,
        "Склади відповідне клопотання відповідно до норм процесуального права України. Мова: українська.",
    )
    motion_name = MOTION_TYPES.get(motion_type, "Клопотання")
    user = (
        f"## ДАНІ СПРАВИ\n{case_data}\n\n"
        f"## ЗАВДАННЯ\n"
        f"Склади '{motion_name}'.\n"
        f"Формат: готовий юридичний документ українською мовою."
    )
    return system, user
